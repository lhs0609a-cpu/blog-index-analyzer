def _generate_unique_comment(existing_comments: Set[str], category: str = None) -> str:
    """카테고리에 맞는 고유 댓글 생성"""
    max_attempts = 50

    # 카테고리별 댓글 가져오기 (70% 확률), 범용 댓글 (30% 확률)
    if category and category in COMMENT_TEMPLATES_BY_CATEGORY and random.random() < 0.7:
        pool = COMMENT_TEMPLATES_BY_CATEGORY[category]
    else:
        pool = COMMENT_TEMPLATES_GENERAL

    for _ in range(max_attempts):
        base_comment = random.choice(pool)
        suffix = random.choice(COMMENT_SUFFIXES)
        comment = f"{base_comment}{suffix}".strip()

        if comment not in existing_comments:
            return comment

    base_comment = random.choice(pool)
    unique_id = random.randint(1, 9999)
    return f"{base_comment} #{unique_id}"


def _check_title_exists_in_db(title: str) -> bool:
    """DB에서 제목 중복 확인 (정확 매치 + 유사 매치)"""
    from database.community_db import get_db_connection
    try:
        conn = get_db_connection()
        cursor = conn.cursor()
        # 정확 매치
        cursor.execute("SELECT 1 FROM posts WHERE title = ? AND is_deleted = FALSE LIMIT 1", (title,))
        if cursor.fetchone():
            conn.close()
            return True
        # 유사 매치 (앞 15자 일치)
        title_prefix = title.strip()[:15]
        if len(title_prefix) >= 8:
            cursor.execute(
                "SELECT 1 FROM posts WHERE title LIKE ? AND is_deleted = FALSE LIMIT 1",
                (f"{title_prefix}%",),
            )
            if cursor.fetchone():
                conn.close()
                return True
        conn.close()
        return False
    except:
        return False


def _generate_unique_title(template: Dict, attempt: int) -> str:
    """제목 변형 시스템: 내장된 제목 변형 목록에서 선택"""
    titles = template.get("titles", [template.get("title", "블로그 이야기")])

    if attempt < len(titles):
        return titles[attempt]

    # 변형 목록 소진 시 접미사 추가
    base = random.choice(titles)
    suffix_patterns = [
        f" (feat. 경험담)", f" - 솔직 후기", f" (업데이트)",
        f" | 후기", f" + 팁", f" (공유)",
    ]
    idx = (attempt - len(titles)) % len(suffix_patterns)
    return f"{base}{suffix_patterns[idx]}"


def get_random_post(category: str = None) -> Dict:
    """중복 없는 랜덤 게시글 생성"""
    if category is None:
        categories = ["free", "tip", "question", "success"]
        weights = [40, 25, 25, 10]
        category = random.choices(categories, weights=weights)[0]

    templates = POST_TEMPLATES.get(category, POST_TEMPLATES["free"])
    random.shuffle(templates)

    for template in templates:
        for attempt in range(10):
            title = _generate_unique_title(template, attempt)

            if not _check_title_exists_in_db(title):
                content = template["content"]
                if random.random() < 0.3:
                    extras = [
                        "궁금한 점 있으면 댓글로 편하게 물어보세요!",
                        "다들 비슷한 경험 있으시면 공유해주세요",
                        "의견이나 조언 부탁드려요!",
                        "공감하시면 좋아요 눌러주세요 ㅎㅎ",
                    ]
                    content = content + f"\n\n{random.choice(extras)}"

                tags = template.get("tags", [])

                return {
                    "title": title,
                    "content": content,
                    "category": category,
                    "author_name": get_random_blogger_name(),
                    "tags": tags,
                }

    # 모든 시도 실패 시
    unique_id = random.randint(100000, 999999)
    return {
        "title": f"블로그 이야기 #{unique_id}",
        "content": "블로그 하면서 느끼는 것들을 공유해봅니다\n\n"
                   "다들 오늘도 블로그 열심히 하시나요?\n"
                   "함께 성장하면 좋겠어요!",
        "category": category,
        "author_name": get_random_blogger_name(),
        "tags": ["블로그일상"],
    }


