def generate_seed_posts(count: int = 30) -> List[int]:
    """시드 게시글 생성"""
    from database.community_db import get_db_connection
    import json

    conn = get_db_connection()
    cursor = conn.cursor()

    created_ids = []

    for i in range(count):
        post = get_random_post()
        fake_user_id = random.randint(10000, 99999)

        days_ago = random.randint(0, 180)
        hours_ago = random.randint(0, 23)
        created_at = (datetime.now() - timedelta(days=days_ago, hours=hours_ago)).isoformat()

        views = random.randint(10, 500)
        likes = random.randint(0, min(views // 8, 25))

        tags_json = json.dumps(post.get("tags", []), ensure_ascii=False)

        cursor.execute("""
            INSERT INTO posts (user_id, user_name, title, content, category, views, likes, tags, created_at)
            VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)
        """, (fake_user_id, post["author_name"], post["title"], post["content"],
              post["category"], views, likes, tags_json, created_at))

        created_ids.append(cursor.lastrowid)

    conn.commit()
    conn.close()

    logger.info(f"Generated {len(created_ids)} seed posts")
    return created_ids


def generate_seed_comments(post_ids: List[int] = None, comments_per_post: tuple = (2, 8)) -> int:
    """시드 댓글 생성 - 카테고리별 맥락 있는 댓글"""
    from database.community_db import get_db_connection

    conn = get_db_connection()
    cursor = conn.cursor()

    if not post_ids:
        cursor.execute("SELECT id FROM posts ORDER BY created_at DESC LIMIT 100")
        post_ids = [row['id'] for row in cursor.fetchall()]

    total_comments = 0

    for post_id in post_ids:
        # 게시글 카테고리 조회
        cursor.execute("SELECT category FROM posts WHERE id = ?", (post_id,))
        row = cursor.fetchone()
        post_category = row['category'] if row else 'free'

        num_comments = random.randint(comments_per_post[0], comments_per_post[1])
        existing_comments: Set[str] = set()

        for _ in range(num_comments):
            fake_user_id = random.randint(10000, 99999)
            author_name = get_random_blogger_name()

            comment = _generate_unique_comment(existing_comments, post_category)
            existing_comments.add(comment)

            hours_ago = random.randint(0, 72)
            created_at = (datetime.now() - timedelta(hours=hours_ago)).isoformat()

            cursor.execute("""
                INSERT INTO post_comments (post_id, user_id, user_name, content, created_at)
                VALUES (?, ?, ?, ?, ?)
            """, (post_id, fake_user_id, author_name, comment, created_at))

            total_comments += 1

        cursor.execute("""
            UPDATE posts SET comments_count = (
                SELECT COUNT(*) FROM post_comments WHERE post_id = ? AND is_deleted = FALSE
            ) WHERE id = ?
        """, (post_id, post_id))

    conn.commit()
    conn.close()

    logger.info(f"Generated {total_comments} seed comments")
    return total_comments


def initialize_community() -> Dict:
    """커뮤니티 초기화 (기존 데이터 삭제 후 새로 생성)"""
    logger.info("Initializing community with high-quality data...")

    clear_all_community_data()

    post_ids = generate_seed_posts(50)
    comments_count = generate_seed_comments(post_ids, (2, 8))

    return {
        "posts_created": len(post_ids),
        "comments_created": comments_count,
        "message": "v6-high-quality-templates"
    }


def generate_daily_content() -> Dict:
    """매일 자동 콘텐츠 생성"""
    from database.community_db import get_db_connection

    conn = get_db_connection()
    cursor = conn.cursor()

    result = {"posts_created": 0, "comments_created": 0}

    num_posts = random.randint(3, 5)
    new_post_ids = []

    for _ in range(num_posts):
        post = get_random_post()
        fake_user_id = random.randint(10000, 99999)
        hours_ago = random.randint(0, 12)
        created_at = (datetime.now() - timedelta(hours=hours_ago)).isoformat()
        import json
        tags_json = json.dumps(post.get("tags", []), ensure_ascii=False)

        cursor.execute("""
            INSERT INTO posts (user_id, user_name, title, content, category, tags, created_at)
            VALUES (?, ?, ?, ?, ?, ?, ?)
        """, (fake_user_id, post["author_name"], post["title"], post["content"],
              post["category"], tags_json, created_at))

        new_post_ids.append(cursor.lastrowid)
        result["posts_created"] += 1

    conn.commit()

    cursor.execute("SELECT id, category FROM posts WHERE is_deleted = FALSE ORDER BY created_at DESC LIMIT 30")
    recent_posts = [(row['id'], row['category']) for row in cursor.fetchall()]

    post_existing_comments: Dict[int, Set[str]] = {}
    for post_id, _ in recent_posts:
        cursor.execute("SELECT content FROM post_comments WHERE post_id = ?", (post_id,))
        post_existing_comments[post_id] = {row['content'] for row in cursor.fetchall()}

    num_comments = random.randint(10, 20)
    for _ in range(num_comments):
        if not recent_posts:
            continue
        post_id, post_category = random.choice(recent_posts)

        fake_user_id = random.randint(10000, 99999)
        author_name = get_random_blogger_name()

        existing = post_existing_comments.get(post_id, set())
        comment = _generate_unique_comment(existing, post_category)
        existing.add(comment)

        cursor.execute("""
            INSERT INTO post_comments (post_id, user_id, user_name, content)
            VALUES (?, ?, ?, ?)
        """, (post_id, fake_user_id, author_name, comment))

        cursor.execute("""
            UPDATE posts SET comments_count = comments_count + 1 WHERE id = ?
        """, (post_id,))

        result["comments_created"] += 1

    conn.commit()
    conn.close()

    logger.info(f"Daily content generated: {result}")
    return result
